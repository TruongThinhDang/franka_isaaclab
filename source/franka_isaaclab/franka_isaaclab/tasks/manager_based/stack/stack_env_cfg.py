@configclass
class ActionsCfg:
    """Action specifications for the MDP."""

    # will be set by agent env cfg
    arm_action: mdp.JointPositionActionCfg = MISSING
    gripper_action: mdp.BinaryJointPositionActionCfg = MISSING


@configclass
class ObservationsCfg:
    """Observation specifications for the MDP."""

    @configclass
    class PolicyCfg(ObsGroup):
        """Observations for policy group with state values."""

        actions = ObsTerm(func=mdp.last_action)
        joint_pos = ObsTerm(func=mdp.joint_pos_rel)
        joint_vel = ObsTerm(func=mdp.joint_vel_rel)
        object = ObsTerm(func=mdp.object_obs)
        cube_positions = ObsTerm(func=mdp.cube_positions_in_world_frame)
        cube_orientations = ObsTerm(func=mdp.cube_orientations_in_world_frame)
        eef_pos = ObsTerm(func=mdp.ee_frame_pos)
        eef_quat = ObsTerm(func=mdp.ee_frame_quat)
        gripper_pos = ObsTerm(func=mdp.gripper_pos)

        def __post_init__(self):
            self.enable_corruption = True
            self.concatenate_terms = True

    # observation groups
    policy: PolicyCfg = PolicyCfg()


@configclass
class RewardsCfg:
    """Reward terms for the MDP."""

    # Stage 1: Reach cube_1 (giảm weight)
    reaching_object = RewTerm(
        func=mdp.object_ee_distance, 
        params={"std": 0.1}, 
        weight=0.5  # GIẢM từ 1.0 → 0.5
    )

    # Stage 2: Grasp and lift cube_1 (giảm weight)
    cube_grasped = RewTerm(
        func=mdp.cube_grasped,
        params={"diff_threshold": 0.04},
        weight=1.0  # GIẢM từ 1.5 → 1.0
    )

    lifting_object = RewTerm(
        func=mdp.object_is_lifted, 
        params={"minimal_height": 0.08}, 
        weight=1.5  # GIẢM từ 2.5 → 1.5
    )

    # Stage 3: Align cubes for stacking (TĂNG weight)
    cubes_stacking = RewTerm(
        func=mdp.cubes_stacking_reward, 
        params={"std": 0.05}, 
        weight=5.0  # TĂNG từ 3.0 → 5.0
    )

    cube_height_alignment = RewTerm(
        func=mdp.cube_height_alignment,
        params={"std": 0.03, "target_height": 0.0468},
        weight=6.0  # TĂNG từ 3.5 → 6.0
    )

    # Stage 4: Success bonus (TĂNG MẠNH)
    stacking_success = RewTerm(
        func=mdp.stacking_success, 
        weight=25.0  # TĂNG từ 15.0 → 25.0
    )

    # Regularization penalties
    action_rate = RewTerm(
        func=mdp.action_rate_l2, 
        weight=-0.005
    )

    joint_vel = RewTerm(
        func=mdp.joint_vel_l2,
        weight=-0.0001,
        params={"asset_cfg": SceneEntityCfg("robot")},
    )


@configclass
class TerminationsCfg:
    """Termination terms for the MDP."""

    time_out = DoneTerm(func=mdp.time_out, time_out=True)

    cube_1_dropping = DoneTerm(
        func=mdp.root_height_below_minimum, params={"minimum_height": -0.05, "asset_cfg": SceneEntityCfg("cube_1")}
    )

    cube_2_dropping = DoneTerm(
        func=mdp.root_height_below_minimum, params={"minimum_height": -0.05, "asset_cfg": SceneEntityCfg("cube_2")}
    )

    success = DoneTerm(func=mdp.cubes_stacked)


@configclass
class StackEnvCfg(ManagerBasedRLEnvCfg):
    """Configuration for the stacking environment."""

    # Scene settings
    scene: ObjectTableSceneCfg = ObjectTableSceneCfg(num_envs=4096, env_spacing=2.5, replicate_physics=False)
    # Basic settings
    observations: ObservationsCfg = ObservationsCfg()
    actions: ActionsCfg = ActionsCfg()
    rewards: RewardsCfg = RewardsCfg()
    # MDP settings
    terminations: TerminationsCfg = TerminationsCfg()


    # Unused managers
    commands = None
    events = None
    curriculum = None

    def __post_init__(self):
        """Post initialization."""
        # general settings
        self.decimation = 5
        self.episode_length_s = 40.0  # TĂNG từ 30.0 → 40.0
        # simulation settings
        self.sim.dt = 0.01  # 100Hz
        self.sim.render_interval = 2

        self.sim.physx.bounce_threshold_velocity = 0.2
        self.sim.physx.bounce_threshold_velocity = 0.01
        self.sim.physx.gpu_found_lost_aggregate_pairs_capacity = 1024 * 1024 * 4
        self.sim.physx.gpu_total_aggregate_pairs_capacity = 16 * 1024
        self.sim.physx.friction_correlation_distance = 0.00625